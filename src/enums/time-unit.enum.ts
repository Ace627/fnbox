/**
 * 时间单位常量（均以秒为基准）
 * 所有值均表示对应时间单位包含的秒数，便于时间计算
 * 注意：月、季、年等采用平均天数计算（月=30天，年=365天），非精确历法
 */
export const TimeUnit = {
  /** 一秒 */
  SECOND: 1,

  /** 一分钟 */
  MINUTE: 60,

  /** 半分钟 */
  HALF_MINUTE: 30,

  /** 一小时 */
  HOUR: 60 * 60,

  /** 半小时 */
  HALF_HOUR: 30 * 60,

  /** 一天 */
  DAY: 24 * 60 * 60,

  /** 半天 */
  HALF_DAY: 12 * 60 * 60,

  /** 一周 */
  WEEK: 7 * 24 * 60 * 60,

  /** 一月 按 30 天计算 */
  MONTH: 30 * 24 * 60 * 60,

  /** 半月 按 15 天计算 */
  HALF_MONTH: 15 * 24 * 60 * 60,

  /** 一季 按三个月计算 */
  QUARTER: 3 * 30 * 24 * 60 * 60,

  /** 一年 按 365 天计算 */
  YEAR: 365 * 24 * 60 * 60,

  /** 半年 按六个月计算 */
  HALF_YEAR: 180 * 24 * 60 * 60,
} as const
